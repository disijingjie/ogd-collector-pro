def insert_after(content, marker, insert_text):
    """在marker所在段落后插入内容"""
    if marker not in content:
        return content, False
    pos = content.find(marker)
    # 找到marker所在段落的结束位置（下一个空行或下一个标题）
    search_start = pos + len(marker)
    next_heading = content.find('\n#', search_start)
    next_blank = content.find('\n\n', search_start)
    
    if next_heading != -1 and (next_blank == -1 or next_heading < next_blank):
        insert_pos = next_heading
    elif next_blank != -1:
        insert_pos = next_blank + 2
    else:
        insert_pos = len(content)
    
    return content[:insert_pos] + insert_text + content[insert_pos:], True

